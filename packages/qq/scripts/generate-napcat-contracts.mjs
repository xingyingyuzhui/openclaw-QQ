#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const OUT_DIR = path.join(ROOT, "src", "napcat", "contracts", "generated");
const LLMS_URL = "https://napcat.apifox.cn/llms.txt";

function sh(cmd, args) {
  return execFileSync(cmd, args, { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] });
}

function fetchText(url) {
  return sh("curl", ["-fsSL", url]);
}

function unique(arr) {
  return Array.from(new Set(arr));
}

function extractDocUrls(llmsText) {
  const urls = llmsText.match(/https:\/\/napcat\.apifox\.cn\/[0-9a-z]+\.md/g) || [];
  return unique(urls);
}

function extractActionsFromDoc(docText) {
  const out = [];
  const lines = String(docText || "").split("\n").slice(0, 220);
  for (const line of lines) {
    const m = line.match(/^\s*\/([a-z0-9_]+):\s*$/i);
    if (m?.[1]) out.push(m[1]);
  }
  return out;
}

function toTsArray(items) {
  return items.map((it) => `  "${it}",`).join("\n");
}

function toMapInterface(name, actions) {
  const rows = actions
    .map((a) => `  ${JSON.stringify(a)}: Record<string, unknown>;`)
    .join("\n");
  return `export interface ${name} {\n${rows}\n}`;
}

function buildActionsTs(actions, meta) {
  return `/* eslint-disable */\n// Auto-generated by scripts/generate-napcat-contracts.mjs\n// source: ${LLMS_URL}\n// generated_at: ${meta.generatedAt}\n// docs_scanned: ${meta.docs}\n// actions: ${actions.length}\n\nexport const NAPCAT_ACTIONS = [\n${toTsArray(actions)}\n] as const;\n\nexport type NapCatGeneratedAction = (typeof NAPCAT_ACTIONS)[number];\n`;
}

function buildMapsTs(actions, meta) {
  return `/* eslint-disable */\n// Auto-generated by scripts/generate-napcat-contracts.mjs\n// source: ${LLMS_URL}\n// generated_at: ${meta.generatedAt}\n// docs_scanned: ${meta.docs}\n// actions: ${actions.length}\n\n${toMapInterface("NapCatGeneratedRequestMap", actions)}\n\n${toMapInterface("NapCatGeneratedResponseMap", actions)}\n`;
}

function main() {
  const llms = fetchText(LLMS_URL);
  const urls = extractDocUrls(llms);
  const actions = [];

  for (const url of urls) {
    try {
      const doc = fetchText(url);
      actions.push(...extractActionsFromDoc(doc));
    } catch {
      // ignore single-doc fetch failures; keep generation resilient
    }
  }

  const dedupActions = unique(actions).sort();
  const meta = {
    generatedAt: new Date().toISOString(),
    docs: urls.length,
  };

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(path.join(OUT_DIR, "actions.ts"), buildActionsTs(dedupActions, meta), "utf8");
  fs.writeFileSync(path.join(OUT_DIR, "maps.ts"), buildMapsTs(dedupActions, meta), "utf8");
  console.log(`generated actions=${dedupActions.length} docs=${urls.length}`);
}

main();
